-module('xs_token_SUITE').

-include_lib("common_test/include/ct.hrl").

-export([
    all/0,
    groups/0,
    suite/0
]).

-export([
    init_per_suite/1,
    init_per_group/2,
    end_per_group/2,
    end_per_suite/1
]).

-export(['cbcl-token-001'/1]).
-export(['cbcl-token-002'/1]).
-export(['cbcl-token-002b'/1]).
-export(['cbcl-token-003'/1]).
-export(['cbcl-token-004'/1]).
-export(['cbcl-token-005'/1]).
-export(['cbcl-token-006'/1]).

suite() -> [{timetrap, {seconds, 180}}].

init_per_group(_, Config) -> Config.

end_per_group(_, _Config) ->
    xqerl_code_server:unload(all).

end_per_suite(_Config) ->
    ct:timetrap({seconds, 60}),
    xqerl_code_server:unload(all).

init_per_suite(Config) ->
    {ok, _} = application:ensure_all_started(xqerl),
    DD = filename:dirname(filename:dirname(filename:dirname(?config(data_dir, Config)))),
    TD = filename:join(DD, "QT3-test-suite"),
    __BaseDir = filename:join(TD, "xs"),
    [{base_dir, __BaseDir} | Config].

all() ->
    [
        {group, group_0}
    ].

groups() ->
    [
        {group_0, [parallel], [
            'cbcl-token-001',
            'cbcl-token-002',
            'cbcl-token-002b',
            'cbcl-token-003',
            'cbcl-token-004',
            'cbcl-token-005',
            'cbcl-token-006'
        ]}
    ].

'cbcl-token-001'(Config) ->
    __BaseDir = ?config(base_dir, Config),
    Qry = "xs:token(xs:token(\"test\"))",
    Qry1 = Qry,
    io:format("Qry1: ~p~n", [Qry1]),
    Res =
        try
            Mod = xqerl_code_server:compile(filename:join(__BaseDir, "cbcl-token-001.xq"), Qry1),
            xqerl:run(Mod)
        of
            D -> D
        catch
            _:E -> E
        end,
    Out =
        case xqerl_test:assert_string_value(Res, "test") of
            true -> {comment, "String correct"};
            {false, F} -> F
        end,
    case Out of
        {comment, C} -> {comment, C};
        Err -> ct:fail(Err)
    end.

